        <a href="/wonders/2" style="text-decoration: none; color: white"><h1>Висячие сады Семирамиды</h1></a>
        <p style="color: whitesmoke">Ирак, ~Багдад</p>
        <p>
            По легенде, сады были построены вавилонским царем Навуходоносором для своей жены Амитис. По другой версии, их соорудили благодаря царице Семирамиде, правившей в Вавилоне, как предполагают, в IX веке до нашей эры.
            Некоторые версии ставят под сомнение существование садов, поскольку никаких вавилонских текстов, где бы они упоминались, не сохранилось. Их описания встречаются лишь у поздних древнегреческих и римских авторов. Археологических подтверждений существования садов Семирамиды тоже обнаружено не было.
        </p>
    </div>

