        <a href="/wonders/3" style="text-decoration: none; color: white"><h1>Статуя Зевса в Олимпии</h1></a>
        <p style="color: whitesmoke">Греция</p>
        <p>
            Статуя Зевса была возведена в V веке до нашей эры в одноименном храме города Олимпия. Ни статуя, ни храм до наших дней не дошли, но по сохранившимся описаниям известно, что статуя изображала бога-громовержца, сидящего на троне со скипетром и фигурой богини победы Ники. 
            Его ноги покоились на скамейке, поддерживаемой львами, а голову украшал позолоченный венец.
