            Внутри пирамиды находится три камеры, расположенные одна над другой. Камеры, судя по всему, планировалось использовать как усыпальницы, однако две из них так и не были закончены.
            Одной из загадок пирамиды является восходящий проход с тремя гранитными «пробками», которые невозможно сдвинуть, а их назначение остается неизвестным.
